"""
Count from 1 to 10: Use a for loop to print the numbers 1 through 10.

For Loop:
    - for loop is a programming construct that allows you to execute a block of code a specific number of times.
    - Think of it as a way to automate a repetitive task. Instead of writing the same code over and over again
    - The two main scenarios where a for loop is used are:
            - Iterating over a sequence: This is when you have a collection of items (like a list of numbers, a string of characters, or lines in a file) and you want to do something with each item one by one.
            - Repeating an action a known number of times: If you know you need to perform a task exactly 10 times, a for loop is the perfect tool for the job.
"""

for number in range(1, 11):
    print(number)



