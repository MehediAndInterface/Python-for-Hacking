"""
Simple Arithmetic: Ask the user for two numbers, then print their sum, difference, product, and quotient.
"""
num_1 = float(input("Please, enter first number: "))
num_2 = float(input("Please, enter second number: "))

print(f"Sum: {num_1 + num_2}")
print(f"Difference: {num_1 - num_2}")
print(f"Product: {num_1 * num_2}")
print(f"quotient: {num_1 / num_2}")
print(type(num_1))
print(type(num_2))
